def kana_to_romaji(text):
    r, i = [], 0
    while i < len(text):
        ch = text[i]
        key2 = ch + (text[i+1] if i+1 < len(text) else '')
        if key2 in _KANA:
            r.append(_KANA[key2]); i += 2
        elif ch in _KANA:
            v = _KANA[ch]
            if v: r.append(v)
            i += 1
        else:
            r.append(ch); i += 1
    return ''.join(r)
